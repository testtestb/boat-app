import streamlit as st

st.title("🎯 BoatPredictor Pro [24場対応版]")

venues = [
    "桐生", "戸田", "江戸川", "平和島", "多摩川", "浜名湖", "蒲郡", "常滑", "津",
    "三国", "びわこ", "住之江", "尼崎", "鳴門", "丸亀", "児島", "宮島", "徳山",
    "下関", "若松", "芦屋", "福岡", "唐津", "大村"
]

venue = st.selectbox("競艇場を選択", venues)
rank_1 = st.selectbox("1号艇の級別", ["A1", "A2", "B1", "B2"])
tenji_1 = st.number_input("1号艇の展示タイム", 6.00, 7.00, 6.60, 0.01)

if st.button("勝負判定"):
    # 鉄板パターン：1号艇がA1級で、かつ展示タイムが6.55以下（非常に良い）
    if rank_1 == "A1" and tenji_1 <= 6.55:
        st.success(f"✅ 【勝負】{venue}は的中期待値が高いです！")
        st.write("買い目：1-23-234")
    # 次点パターン：1号艇がA2以上で、展示タイムが6.60以下
    elif rank_1 in ["A1", "A2"] and tenji_1 <= 6.60:
        st.info(f"💡 【狙い目】{venue}の標準勝負レース")
        st.write("買い目：1-2-3 / 1-3-2")
    # それ以外は「的中率が低い」と判断して見送る
    else:
        st.error("❌ 【見送り】的中率が低いため勝負不可")
        st.write("今の条件では的中率が期待できません。資金を守るため見送りが正解です。")
