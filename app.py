import streamlit as st

st.title("🎯 BoatPredictor Pro [変動解析モデル]")

venues = ["桐生", "戸田", "江戸川", "平和島", "多摩川", "浜名湖", "蒲郡", "常滑", "津",
          "三国", "びわこ", "住之江", "尼崎", "鳴門", "丸亀", "児島", "宮島", "徳山",
          "下関", "若松", "芦屋", "福岡", "唐津", "大村"]

venue = st.selectbox("競艇場を選択", venues)
# 6.40〜6.80の範囲で、0.01刻みで細かい入力が可能
tenji_1 = st.number_input("1号艇の展示タイム", 6.40, 6.80, 6.60, 0.01)
tenji_4 = st.number_input("4号艇の展示タイム", 6.40, 6.80, 6.60, 0.01)

if st.button("的中重視で解析"):
    st.write("---")
    
    # 偏差を計算（0.01秒単位で予想を完全に変える）
    diff = tenji_4 - tenji_1
    
    # 指数化（展示タイムの差を数値化）
    if diff <= -0.05:
        # 4号艇が圧倒的に速いケース
        st.write("### 🧨 【大波乱】4号艇の捲り")
        st.write("買い目：4-1-2 / 4-1-5 / 4-5-1")
    elif diff <= 0.02:
        # 4号艇がそこそこ速いケース
        st.write("### ⚠️ 【展開注意】差し・捲り警戒")
        st.write("買い目：1-4-2 / 1-4-5 / 4-1-2")
    elif diff <= 0.08:
        # 1号艇がやや有利
        st.write("### 💡 【標準】1号艇軸")
        st.write("買い目：1-2-3 / 1-2-5 / 1-3-2")
    else:
        # 1号艇が圧倒的（展示が速い）
        st.write("### ✅ 【鉄板】1号艇信頼")
        st.write("買い目：1-2-3 / 1-3-2 / 1-2-4")
    
    st.info(f"解析：展示差 {diff:.2f}秒を検知しました。")
